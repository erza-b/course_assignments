a = 2
b = 3

addition = a + b
subtraction = a - b
division = a / b
multiplication = a * b
exponent = a ** b
modulus = a % b
floor_division = a // b

print(f"Addition of 2+3 = {addition}")
print(f"Subtraction of 2-3 = {subtraction}")
print(f"Division of 2/3 = {division}")
print(f"Multiplication of 2*3 = {multiplication}")
print(f"Exponent of 2^3 = {exponent}")
print(f"Modulus of 2%3 = {modulus}")
print(f"Floor Division of 2//3 = {floor_division}")


